#include "point.h"
#include <iostream>

/*--------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------*/
Point::Point()
{
 
}
/*--------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------*/
Point::Point(int x , int y)
   : m_x( x ) , m_y( y )
{
 
}
/*--------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------*/
Point::~Point()
{
 
}

/*--------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------*/
int Point::x()const
{
   return m_x; 
}
/*--------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------*/
int Point::y()const
{
   return m_y; 
}
/*--------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------*/
int& Point::x()
{
   return m_x; 
}
/*--------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------*/
int& Point::y()
{
   return m_y; 
}
/*--------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------*/
Point& Point::setX(int x)
{
   m_x = x; 
   return *this;
}
/*--------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------*/
Point& Point::setY(int y)
{
   m_y = y; 
   return *this;
}
/*--------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------*/
std::ostream& operator<<(std::ostream& os, const Point& pointf )
{
   os << "{" << pointf.m_x << "," << pointf.m_y << "}"; 
   return os;
}






















