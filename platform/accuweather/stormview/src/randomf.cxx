#include "randomf.h"

#include <cstdlib>
#include <ctime>
/*--------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------*/
float RandomF::randomF()
{
   //static auto& me = GetInstance();
   return float(std::rand())/float(RAND_MAX); 
}
/*--------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------*/
float RandomF::randomF(float min , float max )
{
   //static auto& me = GetInstance();
   return RandomF::randomF() * ( max - min ) + min;
}
/*--------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------*/
RandomF& RandomF::GetInstance()
{
   static RandomF the_instance; 
   return the_instance;
}
/*--------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------*/
RandomF::RandomF()
{
   std::srand(std::time(0));
}

